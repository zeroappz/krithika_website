							<h4>Introduction</h4>
							<h3>Brushing Techniques</h3>
							<p><b>How important is brushing?</b><br></p>

							<p>Brushing is very important as it keeps our teeth and gums healthy. It helps to clean dirt between the gums and the teeth
							and also between two teeth. Brushing technique should be soft (to message the gums) and firm (to remove dirt).Brushing
							reduces the risk of infection and cavities making both teeth as well as gums strong.</p>

							<p><b>What are the methods of brushing?</b><br></p>

							<p>There are 2 common techniques of brushing i.e. bass and fone’s methods of brusing.Bass technique are used by adults and
							fone’s is for the children.</p>

							<p>Bass technique is for routine use; in this the brush is at 45 degree to the long axis of the teeth. The bristles are
							used to clean the area between the gums and the teeth as well as between two teeth. After this brushing is done with
							front and back strokes along with vibratory action. Posterior and anterior strokes are used for the eating surface.</p>

							<p>Fone’s technique is very effective for children. In this the brush is placed at 90 degree on outer surface. The teeth
							are closed and rotary action of the brush helps to clean the teeth. This technique is easy to follow.</p>



