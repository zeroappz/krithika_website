							<h4>How important is the tooth paste in brushing?</h4>
							<p>Tooth paste contains fluoride which helps to fight plaque, polish teeth, reduces decay and gives a fresh breadth. A good
							tooth paste fights acid attack ,when the acid is created by the bacteria present in the mouth.</p>

							<p><b>Does incorrect brushing have negative effects?</b><br></p>

							<p>Yes ,there can be a number of problems such as tooth staining, gum bleeding and infection, gums get receded which in
							turn makes the teeth look big and the root of the teeth is exposed, bad breathe ,wearing of teeth, teeth becomes
							sensitive etc.</p>

