							<h3>How frequently should we brush the teeth?</h3>
							<p>What is important is the quality of brushing rather than the number of times. But dentists recommend brushing twice a
							day, one time in the morning and second time at night before going to bed. Frequency of brushing depends on individual
							oral condition i.e. formation of plaque, gum diseases, caries risk etc.</p>

