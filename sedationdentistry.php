							<h3>Does one experience any pain?</h3>
							<p>People feel very comfortable and no pain while the dental treatment is going on and feel even better after the treatment
							is over.</p>

							<p><b>What type of sedation is used?</b><br></p>

							<p>General anesthesia is one type of sedation which everyone thinks off, but this requires a machine for breathing. For
							oral surgeries IV sedation is used. The most convenient, safe and comfortable is oral sedation. In this a pill is given
							to the patient a few hours prior to the visit to the dental clinic.</p>

							<h4>What are different levels of sedation?</h4>

								<p>Mild Sedation in which one is conscious and responds to verbal command. Heart functioning and breathing is normal and
								unaffected .Coordination and cognitive functions can be impaired.</p>

								<p>Moderate Sedation in which there is depression of consciousness and the patient is forced to respond to command.
								Breathing and functioning of heart is not affected.</p>

								<p>Deep Sedation in which patient cannot be easily aroused and there is depression of consciousness. Patient responds
								purposefully. Breathing may be impaired, and heart functions normally.</p>
							<p>General Anesthesia in which there is complete loss of consciousness and the patient cannot be aroused. Breathing is
							impaired and there is the need of ventilator. Heart functioning can be impaired.</p>
								<div class="services-img-1">
									<img src="assets/images/services/sedation-dentistry-section-3.png" alt="Image">

								</div>


							<h4>What type of medicine is given in oral sedation?</h4>
							<p>The dentists give a blue color pill called Triazolam which belongs to the family of Valium. Half pill is used within 2
							hours; hence this medicine has got a short duration. It is very safe and there is no case of emergency.</p>

							<p><b>What feeling one experience after taking the pill?</b><br></p>

							<p>The dentist will give only that must sedation as is required for an individual .One feels completely relaxed and unaware
							of what is going on, as if he is sleeping.</p>

							<p><b>Does a person remember anything of the treatment?</b><br></p>

							<p>After the treatment the patient forgets the memories of the dental clinic.</p>

							<h4>How safe is sedation dentistry?</h4>
							<p>Oral sedation has been used for more than 30 years. It is very safe and comfortable. Firstly the dentist checks
							individuals’ medical past and then he makes the patient understand how sedation will work, so that the patient feels
							comfortable. During the treatment the patient’s oxygen saturation, blood pressure and heart rate are monitored
							continuously. Dentists also have reversal agent that ends the effect of sedation within 30 seconds.</p>

							<p><b>What are the side effects?</b><br></p>

							<p>Side effects such as hiccups and dryness of the mouth can be felt after the treatment is over. But if one does not
							experience such side effects, he will be completely unaware of sedation. Sedation is very beneficial for people who face
							problem in getting numb.</p>



