							<h4>Introduction</h4>
							<h3>Cosmetic Dentistry</h3>
							<p><b>What is the meaning of the term cosmetic dentistry?</b><br></p>

							<p>Cosmetic dentistry is the branch of science which works on enhancing the appearance of the teeth. It improves aesthetics of one’s teeth by using various techniques such as teeth whitening, dental veneers, invisalign, tmj treatment. Moreover cosmetic dentistry helps to keep good oral hygiene and healthy teeth.</p>

							<h3>What are teeth whitening?</h3>


							<Third>Sometimes due to bad eating habits , consumption of alcohol, smoking, intake of fluoride, red wine, colorful beverages etc causes discoloration of teeth. Stained teeth hamper ones appearance and smile. With the help of teeth whitening technique the impurities are oxidized. Hard stains are difficult to remove with brush, but they need proper treatment in which the oxidizing agents come in contact with the teeth. Special care should be taken as oxidizing agents are harmful for the gums. A qualified and a skilled doctor can only perform this technique.

							<p><b>What are veneers?</b><br></p>

							<p>Veneers are a thin layer made of porcelain which is bonded to the front of the teeth by reducing the enamel. Veneer is wafer like and it matches the color of the surrounding teeth. It helps to conceal deformed/chipped teeth or yellow teeth. It is a technique to improve one’s appearance and get an attractive smile.</p>

