							<h4>Introduction</h4>
							<h3>Teeth Whitening</h3>
							<p><b>What is teeth whitening?</b><br></p>

							<p>Teeth whitening is a process of removing stains that stick on to the enamel, it is bleaching process used to get rid of the discoloration of the teeth. There are different ways of teeth whitening, some people go for bleaching sessions at the dental clinic, some buy over –the –counter products and very few people go for cosmetic treatment. Teeth whitening treatment is not permanent, though the results are effective and noticeable. It requires a lot of care and oral hygiene.</p>

							<p><b>What are tooth stains?</b><br></p>

							<p>
								There are 2 types of staining of teeth: extrinsic and intrinsic staining.<br>

								Extrinsic stains appear on the teeth surface .The main reason for this is exposure/drinking dark-colored beverages, tobacco, food and routine wearing of teeth. Minor stains can be removed while brushing .Some stains are stubborn and require teeth bleaching. Persistent stains penetrate deep inside the teeth and require special efforts to get removed and if proper attention is not paid such stains can become permanent.<br>

								Intrinsic stains appear inside the teeth. Such stains result from aging, trauma, exposure to minerals at the time of tooth formation /excessive intake of fluoride through injection. Earlier it was believed that intrinsic stains could not be removed by bleaching, but now cosmetic dentistry has changed this thinking and doctors believe that deep stains in the teeth can also be removed with the help of take home teeth whitening and regular supervision of the dentist.
							</p>


							<p><b>What results in stains?</b><br></p>

							<p>Various factors cause stains such as:-</p>
							<ul>
								<li>
									<!-- <p><b>Why teeth become irregular?</b><br></p> -->
									<span class="bulletPoints"></span>
									AGE- The color of the teeth depends upon the age, as one grows older the color of the teeth becomes darker and the stains become stubborn e.g. Teeth whitening will show dramatic results for teenagers but the results change at the age of 50 because the stains become stubborn.
								</li>
								<li>
									<!-- <p><b>At what age one can go for orthodontic treatment?</b><br></p> -->
									<span class="bulletPoints"></span>
									Eating habits-consumption of red wine, tea, coffee, carrot, colas, oranges and deep colored beverages causes staining of teeth over year’s .Moreover citrus fruits as well as vinegar results in erosion of the enamel.
								</li>
								<li>
									<!-- <p><b>At what age one can go for orthodontic treatment?</b><br></p> -->
									<span class="bulletPoints"></span>
									Smoking habits results in intrinsic stains.
								</li>
								<li>
									<!-- <p><b>At what age one can go for orthodontic treatment?</b><br></p> -->
									<span class="bulletPoints"></span>
									Consumption of drugs/chemicals at the time of tooth formation results in formation of a grey line which is hard to remove.
								</li>
									<li>
										<!-- <p><b>At what age one can go for orthodontic treatment?</b><br></p> -->
										<span class="bulletPoints"></span>
									Grinding of teeth results in micro cracking of teeth by which the tooth edges become dark.
									</li>
							</ul>

