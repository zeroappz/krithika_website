							<h3>What are the teeth whitening options available?</h3>
							<p>There are 3 main whitening options:-</p>

							<p><b>In-Office</b></p>

							<p>Such type of whitening gives significant results in very less time. High concentration of peroxide gel is used on teeth after the gums are protected with applying paint on a rubber dam. Stubborn stains may require one more session.</p>
							<p><b>Take-Home Whitening Kits</b></p>

							<p>Such professional kits use lower concentration of peroxide gel, but gives great results after a long time. The gel is applied with the help of custom made trays (bleaching trays) that look like mouth guards.</p>

							<h3>Over-the-Counter</h3>
							<p>This is the cheapest & convenient way of teeth whitening as it involves buying of whitening kit from the store. This kit has a bleaching gel, which is applied with the help of whitening strips, paints applied on applicators, trays. This method may only whiten the front teeth and not the entire smile.</p>

